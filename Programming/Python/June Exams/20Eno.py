month_strings = ["", "Ιανουάριο", "Φεβρουάριο", "Μάρτιο", "Απρίλιο", "Μαΐο", "Ιούνιο", "Ιούλιο", "Αύγουστο", "Σεπτέμβριο", "Οκτώβριο", "Νοέμβριο", "Δεκέμβριο"]

month = int(input("Δώσε έναν αριθμό από το 1 έως το 12 για να μάθεις σε ποιόν μήνα αντιστοιχεί: "))

while month not in range(0, 13):
    month = int(input("Ο αριθμός που έδωσες δεν είναι απο 1 έως 12. Ξαναδώσε αριθμό: "))

print("Ο αριθμός %d αντιστοιχεί στον μήνα %s" % (month, month_strings[month]))
